package module2;

public class EndsOfStrings {

	public static void main(String[] args) {

		String str1 = "Java Exercises";
		String str2 = "Java Exercise";
		
		System.out.println("'Java Exercises' ends with 'se' ? : " + str1.endsWith("se"));
		System.out.println("'Java Exercise' ends with 'se' ? : " + str2.endsWith("se"));
	}

}
